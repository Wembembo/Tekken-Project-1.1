function renderTables(character, containerID) {
  const container = document.getElementById(containerID);
  container.innerHTML = ""; // Clear previous content

  const tables = data[character];
  if (!tables) {
    container.innerHTML = `<p>No data available for ${character}</p>`;
    return;
  }

  // Define headings for each section
  const sectionTitles = [
    " Key Terminology ",
    " Important Moves ",
    character === "King" || character === "Lars"
      ? " Command Grabs "
      : " Defensive Options ",
  ];

  tables.forEach((tableData, index) => {
    const tableWrapper = document.createElement("div");

    // table titles
    const heading = document.createElement("h2");
    heading.innerText = sectionTitles[index];
    tableWrapper.appendChild(heading);

    const table = document.createElement("table");

    // table headers
    const headerRow = document.createElement("tr");
    tableData.headers.forEach((header) => {
      const th = document.createElement("th");
      th.innerHTML = header;
      headerRow.appendChild(th);
    });

    table.appendChild(headerRow);

    // table rows and data
    tableData.rows.forEach((row) => {
      const tr = document.createElement("tr");
      row.forEach((cell) => {
        const td = document.createElement("td");

        if (cell.includes("notation")) {
          td.classList.add("notation");
        }

        td.innerHTML = cell;
        tr.appendChild(td);
      });
      table.appendChild(tr);
    });

    tableWrapper.appendChild(table);
    container.appendChild(tableWrapper);
  });
}

